/**
 * f_pstr - prints the string starting at the top of the stacks
 * followed by a new lines of stacks.
 * @head: this is the stack heads.
 * @counter: line_numbers.
 * Return: no return
*/

void f_pstr(stack_t **head, unsigned int counter)
{
	stack_t *h;
	(void)counter;

	h = *head;
	while (h)
	{
		if (h->n > 127 || h->n <= 0)
		{
			break;
		}
		printf("%c", h->n);
		h = h->next;
	}
	printf("\n");
}
